for i in range(5):      #Un parametro indica el final
    print(i)            #i es llamado "indice"

print('---------------------------------------------------')

for j in range(2,8):    #Dos parametros indican inicio y fin
    print(j)            #j es llamado "indice"

print('---------------------------------------------------')

for k in range(3,10,2): #Tres parametros indican inicio, fin y paso
    print(k)            #k es llamdo "indice"

print('---------------------------------------------------')

frase = 'Bienvenidos a todos'

for letra in frase:
    print(letra)

print('---------------------------------------------------')

for iteracion in range(10):
    print(iteracion/10)